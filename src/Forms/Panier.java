    private void printMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_printMouseClicked
        c = "RecuPharmacie";
        req = "select V.CODEVENTE,v.quantite,pu,v.total,Date_operation,nom_produit,nom,[TOTACH].TOTAL from vente v inner join IDENTITE on IDENTITE.ID=v.code_personne inner join\n"
                + "PHARMACIE on PHARMACIE.ID=v.id inner join TOTACH on TOTACH.CODE_PATIENT=IDENTITE.id where TOTACH.ID='" + idach.getText() + "' ";

        if (idach.getText().isEmpty() || tot_achat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Impossible de faire l'impression");
        } else {
             try {
             sy.EtatSortie(req,c);
        } catch (Exception e) {
        }
            sy.Viderpanier();
        }
    }//GEN-LAST:event_printMouseClicked
