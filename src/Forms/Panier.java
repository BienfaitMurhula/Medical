        c = "RapportVente";
        req = "select r.QUANTITE,r.[PRIX UNITAIRE],r.TOTAL as Tot,r.[DATE D'ACHAT],nom_produit,nom+' '+postnom+' '+prenom as patient from RAPPORT_ACHAT r \n"
                + "inner join PHARMACIE p on p.id=r.id_produit inner join IDENTITE on IDENTITE.ID=r.ID_PERSONNE where r.[DATE D'ACHAT] between '" + ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText() + "' and '" + ((JTextField) jDateChooser2.getDateEditor().getUiComponent()).getText() + "'";
